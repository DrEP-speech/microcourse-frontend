const fs = require("fs");
const path = require("path");

const repoRoot = process.cwd();
const e2eDir = path.join(repoRoot, "tests", "e2e");
if (!fs.existsSync(e2eDir)) {
  console.error("Missing tests/e2e folder:", e2eDir);
  process.exit(1);
}

const files = fs.readdirSync(e2eDir).filter(f => f.endsWith(".spec.ts"));
if (!files.length) {
  console.error("No .spec.ts files found in:", e2eDir);
  process.exit(1);
}

function patchFile(fullPath) {
  let s = fs.readFileSync(fullPath, "utf8");
  let changed = false;

  // 1) If file calls /auth/login via request.post, remove that whole login block (best-effort)
  // This targets the common pattern: request.post(.../auth/login...) then expect(res.ok()) then res.json() token extraction.
  const loginBlockRe = /const\s+res\s*=\s*await\s+request\.post\([\s\S]{0,4000}?\/auth\/login[\s\S]{0,4000}?\);\s*[\s\S]{0,4000}?expect\([\s\S]{0,2000}?res\.ok\(\)[\s\S]{0,2000}?\);\s*[\s\S]{0,4000}?const\s+json\s*=\s*await\s+res\.json\(\);\s*[\s\S]{0,2000}?const\s+token\s*=\s*json\.token[\s\S]{0,2000}?\;\s*/m;

  if (loginBlockRe.test(s)) {
    s = s.replace(loginBlockRe, `// NOTE: E2E uses seeded token (no password login)
// Load token from tests/seed-artifacts.json (generated by backend seed script)
const fs2 = require("fs");
const path2 = require("path");
const artifacts = JSON.parse(
  fs2.readFileSync(path2.join(process.cwd(), "tests", "seed-artifacts.json"), "utf8")
);

const API_BASE = process.env.E2E_API_BASE || artifacts.apiBase || "http://localhost:4000/api";
const token = artifacts.student && artifacts.student.token;
if (!token) throw new Error("Missing artifacts.student.token in tests/seed-artifacts.json");
`);
    changed = true;
  }

  // 2) Ensure token is injected BEFORE the first page.goto(...)
  // Insert addInitScript right before the first await page.goto(
  const gotoIdx = s.indexOf("await page.goto");
  if (gotoIdx !== -1 && !s.includes("page.addInitScript") && s.includes("const token")) {
    const inject = `// Inject auth token into storage before navigation
await page.addInitScript((t) => {
  try {
    window.localStorage.setItem("token", t);
    window.localStorage.setItem("accessToken", t);
    window.localStorage.setItem("authToken", t);
  } catch (e) {}
}, token);

`;
    s = s.slice(0, gotoIdx) + inject + s.slice(gotoIdx);
    changed = true;
  }

  // 3) Kill any accidental Unicode dashes in the test file itself (rare, but you got bitten already)
  const before = s;
  s = s.replace(/\u2013|\u2014/g, "-").replace(/â€”/g, "-");
  if (s !== before) changed = true;

  if (changed) {
    fs.writeFileSync(fullPath, s, "utf8");
    console.log("PATCHED:", path.relative(repoRoot, fullPath));
  } else {
    console.log("SKIPPED (no match):", path.relative(repoRoot, fullPath));
  }
}

for (const f of files) patchFile(path.join(e2eDir, f));
console.log("Done.");
